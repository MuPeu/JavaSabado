package aula2;

public class TiposPrimitivos {

	public static void main(String[] args) {
		// Exemplo de tipos primitivos em Java
		char sexo = 'F';   //Tipo primitivo char
		byte idade = 89;   //Tipo primitivo byte
		short codigo = 256;   //Tipo primitivo short
		float nota = 4.8f;   //Tipo primitivo float
		int alunos = 100, classes = 20;   //Tipo primitivo int
		long moradores = 9050100;   //Tipo primitivo long
		double dolar = 5.47;   //Tipo primitivo double
		boolean alternativa = false;   //Tipo primitivo boolean
		
		//Processamento e Sa√≠da de dados
		System.out.println("Sexo: " + sexo + " Idade: " + idade + " Codigo: " + codigo);
		System.out.println("Nota: " + nota + " Alunos: " + alunos + " classes: " + classes);
		System.out.println("Moradores: " + moradores + " Dolar: " + dolar + " Alternativa: " + alternativa);
		
	}

}
